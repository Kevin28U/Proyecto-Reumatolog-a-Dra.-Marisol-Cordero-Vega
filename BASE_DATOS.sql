--INSERTAR PACIENTE
CREATE OR ALTER PROCEDURE DBO.SP_INSERTAR_PACIENTE(
	@CEDULA_PACIENTE VARCHAR(50),
	@NOMBRE VARCHAR(100),
	@PRIMER_APELLIDO VARCHAR(100),
	@SEGUNDO_APELLIDO VARCHAR(100),
	@EDAD INT,
	@ID_SEXO INT,
	@ID_OCUPACION INT,
	@ID_DIRECCION INT,
	@FECHA_NACIMIENTO DATE
) AS
BEGIN
	INSERT INTO CLINICA_REUMATOLOGIA.DBO.PACIENTE VALUES(
		@CEDULA_PACIENTE,
		@NOMBRE,
		@PRIMER_APELLIDO,
		@SEGUNDO_APELLIDO,
		@EDAD,
		@ID_SEXO,
		@ID_OCUPACION,
		@ID_DIRECCION,
		@FECHA_NACIMIENTO);
END;
GO


--TABLA USUARIOS
CREATE TABLE USUARIO(
	ID_USUARIO INT IDENTITY(1,1) PRIMARY KEY,
	ID_ROL INT,
	NOMBRE VARCHAR(100),
	PRIMER_APELLIDO VARCHAR(100),
	SEGUNDO_APELLIDO VARCHAR(100),
	CORREO_ELECTRONICO VARCHAR(100),
	NOMBRE_USUARIO VARCHAR(100),
	TELEFONO VARCHAR(50),
	CONTRASENA VARCHAR(100)
);

--TABLA CITA
CREATE TABLE CITA(
	ID_CITA INT IDENTITY(1,1) PRIMARY KEY,
	CEDULA_PACIENTE VARCHAR(50),
	ID_USUARIO INT,
	FECHA DATE,
	HORA TIME,
	MOTIVO VARCHAR(255)
);

--TABLA PACIENTE
CREATE TABLE PACIENTE(
	CEDULA_PACIENTE VARCHAR(50) PRIMARY KEY,
	NOMBRE VARCHAR(100),
	PRIMER_APELLIDO VARCHAR(100),
	SEGUNDO_APELLIDO VARCHAR(100),
	EDAD INT,
	ID_SEXO INT,
	ID_OCUPACION INT,
	ID_DIRECCION INT,
	FECHA_NACIMIENTO DATE
);

--TABLA SEXO
CREATE TABLE SEXO(
	ID_SEXO INT IDENTITY(1,1) PRIMARY KEY,
	SEXO VARCHAR(20)
);

--TABLA OCUPACION
CREATE TABLE OCUPACION(
	ID_OCUPACION INT IDENTITY(1,1) PRIMARY KEY,
	OCUPACION VARCHAR(20)
);

--TABLA DIRECCION
CREATE TABLE DIRECCION(
	ID_DIRECCION INT IDENTITY(1,1) PRIMARY KEY,
	ID_PROVINCIA INT,
	ID_CANTON INT,
	DIRECCION VARCHAR(200)
);

--TABLA PROVINCIA
CREATE TABLE PROVINCIA(
	ID_PROVINCIA INT IDENTITY(1,1) PRIMARY KEY,
	NOMBRE_PROVINCIA VARCHAR(100)
);
--TABLA CANTON
CREATE TABLE CANTON(
	ID_CANTON INT IDENTITY(1,1) PRIMARY KEY,
	ID_PROVINCIA INT,
	NOMBRE_PROVINCIA VARCHAR(100)
);

--TABLA EXPEDIENTE
CREATE TABLE EXPEDIENTE(
	ID_EXPEDIENTE INT IDENTITY(1,1) PRIMARY KEY,
	CEDULA_PACIENTE VARCHAR(50),
	ID_ANTECEDENTES INT,
);

CREATE TABLE ANTECEDENTES(
	ID_ANTECEDENTES INT IDENTITY(1,1) PRIMARY KEY,
	AGO VARCHAR(2000),
	APnoP VARCHAR(2000),
	APP VARCHAR(2000),
	AHF VARCHAR(2000),
	AQTx VARCHAR(2000)
);

CREATE TABLE BITACORA(
	ID_BITACORA INT IDENTITY(1,1) PRIMARY KEY,
	ID_EXPEDIENTE INT,
	PADECIMIENTO_ACTUAL VARCHAR(2000),
	EXAMEN_FISICO VARCHAR(2000),
	IMPRESION_DIAGNOSTICA VARCHAR(2000),
	PLAN_TRATAMIENTO VARCHAR(2000)
);
GO


